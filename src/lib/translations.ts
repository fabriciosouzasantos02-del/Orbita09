// Translates any Portuguese string safely on-the-fly to the requested system Language
export function translateUiText(ptText: string, lang: Language): string {
  if (!ptText) return ptText;
  if (lang === 'pt') return ptText;
  
  if (i18next && i18next.isInitialized) {
    const translation = i18next.t(ptText, { lng: lang });
    if (translation && translation !== ptText) {
      return translation;
    }
  }

  const targetLang = lang as Exclude<Language, 'pt'>;
  const dict = uiTranslations[targetLang];
  if (dict && dict[ptText]) {
    return dict[ptText];
  }
  
  // Try case-insensitive lookup
  const cleanPT = ptText.trim();
  if (dict && dict[cleanPT]) {
    return dict[cleanPT];
  }

  // Handle minor search patterns or fallbacks
  return ptText;
}
