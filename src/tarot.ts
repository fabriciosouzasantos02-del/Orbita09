export function performTarotReading(question: string): { card: TarotCard; isReversed: boolean; position: 'passado' | 'presente' | 'futuro' }[] {
  // Shuffle TAROT_DECK and pick 3 cards
  const shuffled = [...TAROT_DECK].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, 3);
  
  const positions: ('passado' | 'presente' | 'futuro')[] = ['passado', 'presente', 'futuro'];
  
  return selected.map((card, idx) => ({
    card,
    isReversed: Math.random() < 0.25, // 25% chance of card reversal
    position: positions[idx]
  }));
}
